// Einfaches Array = Matrize
// mehrdimensionales Array = Matrix

// Arrays Indizies:
//            0          1          2
// Element Indizes:
//         0  1  2    0  1  2    0  1  2
matrix = [[1, 2, 3], [4, 5, 6], [7, 8, 9]];

console.log(matrix[0][0]); // 1
console.log(matrix[0][2]); // 3
console.log(matrix[1][1]); // 5
console.log(matrix[2][2]); // 9
